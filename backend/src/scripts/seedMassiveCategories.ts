async function seed() {
  try {
    const uri = process.env.MONGODB_URI;
    if (!uri) {
      throw new Error('MONGODB_URI not found in environment');
    }

    console.log('🔌 Connecting to MongoDB...');
    await mongoose.connect(uri);
    console.log('✅ Connected to MongoDB');

    // Clear existing categories
    console.log('🗑️ Clearing existing categories...');
    await Category.deleteMany({});

    // Insert new categories
    console.log('📝 Inserting categories...');

    let totalQuestions = 0;
    for (const cat of allCategories) {
      await Category.create({
        ...cat,
        enabled: true,
        subNameEn: cat.name,
        subNameAr: cat.nameAr,
      });
      totalQuestions += cat.questions.length;
      console.log(`  ✓ ${cat.nameAr} (${cat.questions.length} questions)`);
    }

    console.log(`\n🎉 Successfully seeded ${allCategories.length} categories with ${totalQuestions} total questions!`);
    console.log('\nCategories by section:');
    console.log('  عام (General): معلومات عامة, جغرافيا, علوم');
    console.log('  ترفيه (Entertainment): أفلام, موسيقى, رياضة');
    console.log('  إسلامي (Islamic): قرآن, حديث, فقه, سيرة');
    console.log('  ثقافة (Culture): تاريخ, أدب, تكنولوجيا, ثقافة عربية');

    await mongoose.disconnect();
    console.log('\n👋 Disconnected from MongoDB');
    process.exit(0);
  } catch (error) {
    console.error('❌ Error seeding database:', error);
    process.exit(1);
  }
}

seed();
