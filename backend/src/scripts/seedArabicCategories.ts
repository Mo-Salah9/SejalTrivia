async function seedCategories() {
  try {
    const mongoUri = process.env.MONGODB_URI || 'mongodb://localhost:27017/trivia-game';
    await mongoose.connect(mongoUri);
    console.log('✅ Connected to MongoDB');

    // Delete only the categories we're about to insert (by their IDs)
    const categoryIds = categories.map(c => c.id);
    await Category.deleteMany({ id: { $in: categoryIds } });
    console.log('🗑️  Deleted existing categories with matching IDs');

    // Insert new categories
    await Category.insertMany(categories);

    const totalQuestions = categories.reduce((sum, cat) => sum + cat.questions.length, 0);

    console.log(`\n✅ Added ${categories.length} categories`);
    console.log(`📊 Total questions: ${totalQuestions}`);
    console.log(`📊 Questions per category: 40 (14 easy, 14 medium, 12 hard)`);

    // Verify
    const totalCount = await Category.countDocuments();
    console.log(`\n📊 Total categories in database: ${totalCount}`);

    await mongoose.connection.close();
    console.log('\n🎉 Seeding completed successfully!');
    process.exit(0);
  } catch (error: any) {
    console.error('❌ Error:', error);
    if (mongoose.connection.readyState === 1) {
      await mongoose.connection.close();
    }
    process.exit(1);
  }
}

seedCategories();
